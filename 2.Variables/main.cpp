#include <iostream>

using namespace std;

int main()
{
	// Int
	int x = 2022;
	cout << x << endl;
	
	// Double - Float
	double y = 5.05;
	cout << y << endl;
	
	// Char (must use '')
	char c = 'x';
	cout << c << endl;
	
	// String (must use "")
	string s = "string";
	cout << s << endl;
	
	// Boolean (1->True 0->Flase)
	bool b = true;
	cout << b << endl;
	
	
	cout << "Display variables: " << s << endl;
	
	return 0;
}
